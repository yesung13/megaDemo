package basic.step04;

/*
 * 3. 비교 연산자
 * => 비교 연산자의 결과는 참(true) 또는 거짓(false)이다.
 * 1. 크니?				>
 * 2. 작니?				<
 * 3. 같니?				==
 * 4. 다르니?				!=
 * 5. 크거나 같니?			>=
 * 6. 작거나 같니?			<=
 */

public class Ex01 {

	public static void main(String[] args) {
		// 아래 연산의 결과가 모두 true가 출력되도록 변경

		System.out.println(10 == 3); // 3 == 3
		System.out.println(3 != 8);
		System.out.println(5 > 38); // 5 < 38
		System.out.println(12 < 3); // 12 > 3
		System.out.println(11 >= 11);
		System.out.println(3 <= 10);

	}

}
